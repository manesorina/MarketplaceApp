public class Object {

    int objectID;
    String color;
    int size;
    double price;
    String brand;
    String condition;
    int nrViews;

    public Object(int objectID, String color, int size, double price, String brand, String condition, int nrViews) {
        this.objectID = objectID;
        this.color = color;
        this.size = size;
        this.price = price;
        this.brand = brand;
        this.condition = condition;
        this.nrViews = nrViews;
    }

}

